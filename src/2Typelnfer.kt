
// 变量、常量学习

//var 声明一个变量，变量可改变
//val 声明一个常量，常量不可变

fun main(args:Array<String>){
    var i =18  //计算机给我一块存储空间名称为i，i里面存放的数据类型为整数18
    i = 19
    println(i)

    var j = 99999999999   //计算机给我一块空间名称为j ，j里面存放的数据类型为长整形
    println(j)

    var s = "haha"     //计算机给我一块空间名称为s，s里面存放的数据类型为字符串
    println(s)

    val y = "NO 00001"   //val 声明的变量是不可变的
    println(y)

}